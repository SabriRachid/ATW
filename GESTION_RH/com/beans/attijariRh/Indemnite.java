package com.beans.attijariRh;

import java.util.Date;

public class Indemnite
{
	private float logement;
	private float representation;
	private float deplacement;
	private float transport;
	private Date dateDecision;
	private float autres;
	
	public float getLogement()
	{
		return logement;
	}
	public void setLogement(float logement)
	{
		this.logement = logement;
	}
	public float getRepresentation()
	{
		return representation;
	}
	public void setRepresentation(float representation)
	{
		this.representation = representation;
	}
	public float getDeplacement()
	{
		return deplacement;
	}
	public void setDeplacement(float deplacement)
	{
		this.deplacement = deplacement;
	}
	public float getTransport()
	{
		return transport;
	}
	public void setTransport(float transport)
	{
		this.transport = transport;
	}
	public float getAutres()
	{
		return autres;
	}
	public void setAutres(float autres)
	{
		this.autres = autres;
	}
	public Date getDateDecision()
	{
		return dateDecision;
	}
	public void setDateDecision(Date dateDecision)
	{
		this.dateDecision = dateDecision;
	}
	
	
	
	
}
